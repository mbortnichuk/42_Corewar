#include "libft.h"

void	ft_strrev(char *s)
{
	size_t	i;
	size_t	j;
	char	c;

	if (!s || s[0] == '\0')
		return ;
	i = 0;
	while (s[i])
		i++;
	j = 0;
	while (j < --i)
	{
		c = s[i];
		s[i] = s[j];
		s[j] = c;
		j++;
	}
	return ;
}
